        /*
         * Randomly Generate numbers
         * @param min - smallest value to generate
         * @param max - largest value to generate
         * 
         */
        static public IEnumerable<int> NumGen(int min, int max, int count)
        {
            Random random = new Random();
            for (int i = 0; i <= count; i++)
            {
                yield return random.Next(min, max);   
            }
            yield break;
        }
        /*
         * Generate a sequence of numbers
         * @param count - number of items to generate
         * @param start - the number to start at
         */
        static public IEnumerable<int> SequentialGen(int count, int start = 0)
        {
            Random random = new Random();
            for (int i = start; i <= count; i++)
            {
                yield return i;
            }
            yield break;
        }
        /*
         * Generates a sequential list of numbers and randomize their order
         * @param count - the number of items to generate
         */
        static public IEnumerable<int> RandomizedUnique(int count, int select = -1, int start = 0)
        {
            if (select == -1) select = count;
            List<int> ints = new List<int>();
            int remain = select;
            int[] vals = new int[select];
            Random random = new Random();
            for (int i = 0; i < count; i++)
            {
                ints.Add(i + start);
            }
           
            for(int i = 0; i < select; i++)
            {
                int index = random.Next(0, count - i);
                int result = ints.ElementAt(index);
                ints.RemoveAt(index);
                remain--;
                vals[i] = result;
            }

            for(int i = 0; i < select; i++)
            {
                yield return vals[i];
            }
            yield break;

        }
